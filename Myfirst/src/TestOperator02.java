public class TestOperator02 {
    public static void main(String[] arg){
        int a=3;
        System.out.println(a==3);
        System.out.println(a!=3);
        System.out.println(a<5);
        System.out.println(a>5);

        char b='a';
        char b2='b';
        System.out.println((int)b);
        System.out.println(0+b);
        System.out.println(0+b2);
        System.out.println(b<b2);
    }
}
